public class Car extends Vehicle {
    private int price;
    private int id;

    public Car(){

    }


    public void setPrice (int price) {
        this.price = price;
    }
    public void setId (int id){
        this.id = id;
    }

    public int getPrice () {
        return price;
    }
    public int getId () {
        return id;
    }




}
